export function useHadith() {
    const currentHadith = ref(null)

    // Get hadith based on day of year
    const getHadithOfDay = () => {
        const now = new Date()
        const start = new Date(now.getFullYear(), 0, 0)
        const diff = now - start
        const oneDay = 1000 * 60 * 60 * 24
        const dayOfYear = Math.floor(diff / oneDay)

        const index = dayOfYear % hadiths.length
        currentHadith.value = hadiths[index]
        return currentHadith.value
    }

    // Get random hadith
    const getRandomHadith = () => {
        const randomIndex = Math.floor(Math.random() * hadiths.length)
        currentHadith.value = hadiths[randomIndex]
        return currentHadith.value
    }

    return {
        currentHadith,
        getHadithOfDay,
        getRandomHadith,
        allHadiths: hadiths
    }
}
